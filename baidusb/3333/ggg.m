% 2-D fast Fourier transform
N=64;
a=zeros(N/2);
a(N/8,2*N/8)=1;
ind=[1:N/2,N/2:-1:2];
c=a(ind,ind);
subplot(231)
imshow(c)
subplot(234)
imshow(ifft2(c),[])
%%
col=cos(2*pi*N/8*(1:N-1)/N-1);
row=cos(2*pi*2*N/8*(1:N-1)/N-1);
%%
K=bsxfun(@times,col',row);
subplot(232)
imshow(K,[])
subplot(235)
imshow(abs(fft2(K)),[])
%%
K2=bsxfun(@plus,col',row);
subplot(233)
imshow(K2,[])
subplot(236)
imshow(abs(fft2(K2)),[])
%%
figure
subplot(121)
imshow(abs(fft2(K)))
subplot(122)
imshow(abs(fft2(K2))*1e13)