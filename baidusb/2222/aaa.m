figure 的渲染（renderer） 方式：

web(fullfile(docroot, 'matlab/ref/figure_props.html#Renderer'))

figure('renderer','painters')

painters：默认渲染方式，适于包含 大量，简单，小，相似的对象 的figure；

zbuffer：针对像素的渲染方式，快，准确，耗内存

OpenGL：针对硬件的渲染方式（和显卡交流），最快。动画figure的首选。

 

 

